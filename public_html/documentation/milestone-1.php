		<h2>Bob, Local Business Owner - SubG Stone, 56 yrs. old</h2>
		<img src="BobDobbs-color.png" width="125" height="150"/>
		<h3>"I was obliged to be industrious. Whoever is equally industrious will succeed equally well."</h3>
		<p>
			Bob is the owner of a local stone working company.  His company's website hasn't been substantially updated in almost ten years.  Searching and ordering capabilities will open up new revenue streams.  Bob has built his business from the ground up since the late 1970s and has amassed a large customer database.  SubG Stone has weekly specials on a variety of products.  Currently, customers cannot order stone from the SubG website; customers can fill out a contact form to request information.  Bob believes his customers appreciate his no-nonsense way of doing business and will also appreciate a no-nonsense kind of website.
		</p>
		<h3>Needs</h3>
		<ul>
			<li>See the web developer's existing portfolio.</li>
			<li>Check what programming languages the developer knows.</li>
			<li>See what education the developer has.</li>
		</ul>
		<br>
		<h3>Frustrations</h3>
		<p>
			Because of Bob's concern about losing customers to the big box stores with strong SEO and modern sites he has been compelled to hire a web developer to update his site.  As owner of SubG Stone, Bob likes to be involved in the vetting of prospective employees.  The process is time consuming though so he has the IT manager sift through the applicant's technical qualifications and then send the best candidate's websites to Bob via email.  Bob doesn't know much about coding but he knows a good website when he sees it.  Bob is not inclined to spend a lot of time searching around a programmer's website to see if he will like their work.
		</p>

		<h3>Ideal</h3>
		<ul>
			<li>Bob would like to see the applicant's work with no more than two clicks from the landing page.</li>
			<li>If Bob likes what he sees he wants to find the applicant's contact information quickly.</li>
			<li>Bob doesn't like overly fussy websites.</li>
			<li>Bob wants the company website to be clear and to the point.</li>
		</ul>
