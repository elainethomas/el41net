		<h3>Purpose</h3>
		<p>
			My personal website project will be my first impression for potential employers.  On it's face it will showcase a sample of my front end design abilities.  It will also provide links to my coding portfolio and information about my education and certifications.  Lastly, my availability and contact information will also be detailed.  Eventually, clients would come to my site to hire me for their projects.
		</p>
		<h3>Audience</h3>
		<p>
			This site is designed for three groups of people - employers who want to hire junior web developers, other developers looking for a freelance coder or business partner and customers looking for a web developer with skills.
		</p>

		<h3>Goal</h3>
		<ul>
			<li>Obtain a full or part time job using my web development skills.</li>
			<li>Create a partnership with other developers to work on a coding project.</li>
			<li>Get hired for freelance or contract coding work.</li>
			<li>Act as my "home" on the web to showcase my work and highlight projects I've been a part of.</li>
		</ul>
